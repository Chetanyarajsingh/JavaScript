// while

let index = 0;
while (index <= 10) {
    console.log(`value of index is: ${index}`);
    index += 2 ;
}

let myarr = ['Thor','Iron Man','Captain America']
let i = 0
while (i < myarr.length) {
    console.log(myarr[i]);
    i++;
}

// do-while

let score = 1
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);